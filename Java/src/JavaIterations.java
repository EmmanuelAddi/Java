public class JavaIterations {
    /*
    * while loop
    * do while
    * for
    * for-each
     */
    public static void main(String[] args) {
        //While loop
        int a =4;
        while (a<10){
            System.out.println("Yoh");
            a++;
        }

        //Do...While loop
        do {
            System.out.println("Hello");
            a++;
        } while (a<=10);

    }
}
